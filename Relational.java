public class Relational 
{
	public static void main(String[] args) 
	{
		int a = 5, b = 10;
		
		System.out.println(a < b);
		System.out.println(a > b);
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a <= b);
		System.out.println(a >= b);
	}

}
