package Introduction;
//Q5
public class Arithmetic 
{
	public static void main(String[] args) 
	{
		int a = 10, b = 3;
		
		System.out.println("10 + 3 = " + (a + b));
		System.out.println("10 - 3 = " + (a - b));
		System.out.println("10 / 3 = " + (a / b));
		System.out.println("10 * 3 = " + (a * b));
		System.out.println("10 % 3 = " + (a % b));
	}

}
