public class impexp 
{

	public static void main(String[] args) 
	{
		//Implicit Type Conversion
		int a = 1200;
		double b = a;
		System.out.println(b);
		
		//Explicit Type Conversion
		double c = 3.141597;
		int d = (int) c;
		System.out.println(d);

	}

}
